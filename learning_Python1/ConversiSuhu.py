Reamur = float(input("Masukan suhu reamur :"))

celcius = 0.0
fahrenheit = 0.0
kelvin = 0.0

celcius = 5/4 * Reamur
fahrenheit = 9/4 * Reamur + 32
kelfvin = 5/4 * Reamur + 273.15

print("Reamur %.2f --> suhu celcius %.2f"% (Reamur,celcius))
print("Reamur %.2f --> suhu fahrenheit %.2f"% (Reamur,fahrenheit))
print("Reamur %.2f --> Suhu kelvin %.2f"% (Reamur,kelvin))
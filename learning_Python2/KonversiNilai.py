nilai = float(input("masukan nilai disini : "))

if nilai < 50:
    print("nilai anda E")
elif nilai >= 50 and nilai <= 60:
    print("nilai anda D")
elif nilai >= 60 and nilai <= 70:
    print("nilai anda C")
elif nilai >= 70 and nilai <= 80:
    print("nilai anda B")
elif nilai >= 80 and nilai <= 100:
    print ("nilai anda A")
else:
    print("makanya kalau dikasih tau tuh nurut")